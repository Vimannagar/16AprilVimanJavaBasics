package arraysdiscussion;

public class EnhancedForLoop {

	public static void main(String[] args) {

		int[] i = new int[5];

		i[0] = 10;
		
		i[1] = 20;
		
		i[2] = 30;
		
		i[3] = 40;
		
		i[4] = 10;
		
		//advance for loop or enhanced for loop or for each loop
		
		
		
		for(int d:i)
		{
			System.out.println(d);
		}
		
		
//		WAP to print the value available at even position
		
		
//		WAP to sum up the values of all the elements of an int array. 
		
		
		
		
	}

}
