package loops;

public class WhileLoop {
	
	
	public static void main(String[] args) {
		int i = 0;
		
		while(i<5)
		{
			System.out.println("while loop is executing");
			
			i++;
			
		}
		
		System.out.println("Outside the while loop");
	}

}

