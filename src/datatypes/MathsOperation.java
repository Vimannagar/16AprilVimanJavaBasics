package datatypes;

public class MathsOperation {
	
	public static void main(String[] args) {
		int val1 = 50;
		int val2 = 20;
// Addition		
	int	val3 =  val2 + val1;
	
	System.out.println(val3);//70
	
//	Substraction
	int val4= val2- val1;
	
	System.out.println(val4);//-30
	
//	Multiplication
	
	int i = 10;
	int j = 20;
	
	int k = i*j;
	
	System.out.println(k);//200

//	Division
	
	
	double val5 = val1/val2;
	
	System.out.println(val5);//2.0
	
	double val6 = 50;
	double val7 = 20;
	double val8 = val6/val7;
	
	System.out.println(val8);//2.5
	
	
		
	}

}
